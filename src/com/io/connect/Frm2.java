    @Override
    public void update(Observable o, Object arg) {
        this.txtTexto.append(("COMANDO @USUARIO: " + (String)arg).toUpperCase()+"\n");
        this.txtTexto.append("RESPUESTA SOUND SENSOR: " + evalueMessage((String) arg).toUpperCase()+"\n");
    }
    
      public String evalueMessage(String msg){
        String status="";
        switch(msg){
            case "STATUS":
                status = "FUNCIONANDO\n";
                break;
            case "ON":
                status = "ENCENDIDO\n";
                break;
            case "OFF":
                status = "APAGADO\n";
                break;
            case "SOUND LEVEL":
                status = "ALTO\n";
                break;
            default:
                status = "TACO\n";
        }
        Cliente c = new Cliente(5000, status,"192.168.1.70");
        Thread t = new Thread(c);
        t.start();
        return status;
    }
    
